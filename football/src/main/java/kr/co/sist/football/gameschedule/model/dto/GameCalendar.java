package kr.co.sist.football.gameschedule.model.dto;

public class GameCalendar {
	private int year;
	private int month;
	private int date;
	private int startDay;
	private int endDay;
	private int start;
	private int newLine;
	private int intToday;
	private String color;
	
	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getYear() {
		return year;
	}
	
	public void setYear(int year) {
		this.year = year;
	}
	
	public int getMonth() {
		return month;
	}
	
	public void setMonth(int month) {
		this.month = month;
	}
	
	public int getDate() {
		return date;
	}
	
	public void setDate(int date) {
		this.date = date;
	}
	
	public int getStartDay() {
		return startDay;
	}
	
	public void setStartDay(int startDay) {
		this.startDay = startDay;
	}
	
	public int getEndDay() {
		return endDay;
	}
	
	public void setEndDay(int endDay) {
		this.endDay = endDay;
	}
	
	public int getStart() {
		return start;
	}
	
	public void setStart(int start) {
		this.start = start;
	}
	
	public int getNewLine() {
		return newLine;
	}
	
	public void setNewLine(int newLine) {
		this.newLine = newLine;
	}
	
	public int getIntToday() {
		return intToday;
	}
	
	public void setIntToday(int intToday) {
		this.intToday = intToday;
	}
	
	
	
	
	
}
